const startGameHtml = "<h2>90's Music Trivia</h2><p >The '90s produced some fantastic songs, but how well can you recognise the hits of the decade, when all you have is a 3 minutes! Don't get one wrong or your time will subtract by 5 seconds! When you are ready click the start button to start!</p><button id='start'>start</button>";
const questionBox = document.getElementById("questionbox");
const answerA = document.getElementById("answerA");
const answerB = document.getElementById("answerB");
const answerC = document.getElementById("answerC");
const answerD = document.getElementById("answerD");
const enterScore = document.getElementById("inputScore");
const highScore = document.getElementById("highScores");
const choices = document.getElementById("choices");
const message = document.getElementById("message");
const score = document.getElementById("score");
const title = document.getElementsByClassName("title");
const timeBox = document.getElementById("time");
const finalScore = document.getElementById("finalScore");

// variables
var wrongAnswers = 0;
var correctAnswers = 0;
var questionNumber = 0;
var timeCounter = 0;

// stuff i want to show right away and other stuff i dont want to show
enterScore.style.display = "none";
highScore.style.display = "none";
choices.style.display = "none";

questionBox.style.visibility = "visible";
questionBox.innerHTML = startGameHtml;

// a couple event listeners
var startbutton = document.getElementById("start");
startbutton.addEventListener("click", startGame);

// how it clicks through the questions
function bringit() {
    var currentQuestion = question[questionNumber];
    questionBox.innerText = currentQuestion.q;

    answerA.innerText = currentQuestion.a[0].text;
    answerB.innerText = currentQuestion.a[1].text;
    answerC.innerText = currentQuestion.a[2].text;
    answerD.innerText = currentQuestion.a[3].text;

    answerA.value = currentQuestion.a[0].isCorrect;
    answerB.value = currentQuestion.a[1].isCorrect;
    answerC.value = currentQuestion.a[2].isCorrect;
    answerD.value = currentQuestion.a[3].isCorrect;

    answerA.addEventListener("click", answerButtonClicked);
    answerB.addEventListener("click", answerButtonClicked);
    answerC.addEventListener("click", answerButtonClicked);
    answerD.addEventListener("click", answerButtonClicked);
}
// what happens when you click the right or wrong answer
function answerButtonClicked() {
    if (this.value === "true") {
        correctAnswers++;
    } else {
        wrongAnswers++;
        timeCounter-=5;
    }
    score.innerText = correctAnswers + "/" + wrongAnswers;
    questionNumber++;
    if (questionNumber < question.length) {
        bringit();
    } else {
        clearBoard(true);
    }
}
// pretty self explanatory 
function startGame() {
    timeCounter = 180;

    timeBox.innerText = timeCounter;
    score.innerText = correctAnswers + "/" + wrongAnswers;
    
    choices.style.display = "block";
    bringit();
    var interval = setInterval(function() {
        timeCounter--;
        document.getElementById("time").innerText = timeCounter;

        if (timeCounter === 0)
        {
            clearInterval(interval);
            clearBoard(false);
        }

        if (questionNumber === question.length) {
            clearInterval(interval);
        }
    }, 1000);
}
// what happens when game is over
function clearBoard(allAnswered) {
    answerA.removeEventListener("click", answerButtonClicked);
    answerB.removeEventListener("click", answerButtonClicked);
    answerC.removeEventListener("click", answerButtonClicked);
    answerD.removeEventListener("click", answerButtonClicked);
    questionBox.innerText = "";
    answerA.innerText = "";
    answerB.innerText = "";
    answerC.innerText = "";
    answerD.innerText = "";
    choices.style.display = "none";
    questionBox.style.visibility = "hidden";

    if (allAnswered == true) {
        message.innerText = "COWABUNGA";
    } else {
        message.innerText = "Too Slow Joe!";
    }
    
    finalScore.innerText = correctAnswers + "/" + wrongAnswers;
    enterScore.style.display = "block";
}
// the initials page
function submitButtonClicked() {
    var scoreCards = JSON.parse(localStorage.getItem("scoreCards"));
    if (scoreCards == null) { scoreCards = [] }
    var scoreCard = {
        name: document.getElementById("initials").value,
        score: correctAnswers + "/" + wrongAnswers
    }
    scoreCards.push(scoreCard);
    localStorage.setItem("scoreCards", JSON.stringify(scoreCards));
    finalScore.innerText = "";
    document.getElementById("initials").value = "";
    enterScore.style.display = "none";
    showScoresList();
}
// high scores "page", i call it a page but really shit is hidden 
function showScoresList() {
    highScore.style.display = "block";
    var scoreCards = JSON.parse(localStorage.getItem("scoreCards"));
    var list = "<ul>";
    for (var i = 0; i < scoreCards.length; i++)
    {
        list += "<li>" + scoreCards[i].name + ": " + scoreCards[i].score + "</li>";
    }
    list += "</ul>";
    document.getElementById("scores").innerHTML = list;
}
// this button takes you back to the home page 
function backButtonClicked() {
    questionNumber = 0;
    wrongAnswers = 0;
    correctAnswers = 0;
    timeCounter = 0;
    timeBox.innerText = timeCounter;
    score.innerText = correctAnswers + "/" + wrongAnswers;
    highScore.style.display = "none";
    questionBox.style.visibility = "visible";
    questionBox.innerHTML = startGameHtml;
    startbutton = document.getElementById("start");
    startbutton.addEventListener("click", startGame);
}
// this button clears the high scores list
function clearButtonClicked() {
    localStorage.clear();
    document.getElementById("scores").innerHTML = "";
}
// this is the button thats on the home page that you can click and it will take you the view the high scores
function viewHighScores() {
    questionBox.innerText = "";
    questionBox.style.visibility = "hidden";
    showScoresList();
}
 